----Afișați titlul cărților, numele editurii și numărul total de exemplare disponibile pentru fiecare carte,
-- dar numai pentru acele cărți care au un număr de exemplare disponibile mai mare decât media numărului de exemplare
-- disponibile per carte din întreaga bibliotecă. Ordonare descrescătoare după numărul de exemplare disponibile.

SELECT
    c.titlu AS TitluCarte,
    ed.nume AS NumeEditura,
    COUNT(CASE WHEN ex.este_disponibil = 1 THEN ex.id_exemplar END) AS NumarExemplareDisponibile
FROM
    CARTE c
JOIN
    EDITURA ed ON c.id_editura = ed.id_editura
JOIN
    EXEMPLAR ex ON c.id_carte = ex.id_carte
GROUP BY
    c.titlu, ed.nume, c.id_carte
HAVING
    COUNT(CASE WHEN ex.este_disponibil = 1 THEN ex.id_exemplar END) > (
        SELECT AVG(NumarDisponibilePeCarte)
        FROM (
            SELECT id_carte, COUNT(CASE WHEN este_disponibil = 1 THEN id_exemplar END) AS NumarDisponibilePeCarte
            FROM EXEMPLAR
            GROUP BY id_carte
        ) MediiPeCarte
    )
ORDER BY
    NumarExemplareDisponibile DESC;


--Afișați numele utilizatorului, titlul cărții împrumutate, data împrumutului formatată,
-- și o coloană 'Status Imprumut' care indică dacă împrumutul este 'Returnat la timp', 'Returnat cu intarziere',
-- 'In curs - Termen depasit' sau 'In curs - In termen'. Se va afișa și descrierea penalizării folosind DECODE.
-- Pentru împrumuturile nereturnate, se va considera data curentă pentru evaluarea depășirii termenului.
-- Folosiți NVL pentru a afișa 'Nereturnat' în locul unei date de returnare goale. Ordonare după numele utilizatorului
-- și apoi data împrumutului.

SELECT
    u.nume_complet AS NumeUtilizator,
    c.titlu AS TitluCarte,
    TO_CHAR(i.data_imprumut, 'DD-Mon-YYYY HH24:MI') AS DataImprumutFormatata,
    NVL(TO_CHAR(i.data_returnare, 'DD-Mon-YYYY'), 'Nereturnat') AS DataReturnare,
    CASE
        WHEN i.data_returnare IS NOT NULL AND i.data_returnare <= i.data_scadenta THEN 'Returnat la timp'
        WHEN i.data_returnare IS NOT NULL AND i.data_returnare > i.data_scadenta THEN 'Returnat cu intarziere'
        WHEN i.data_returnare IS NULL AND SYSDATE > i.data_scadenta THEN 'In curs - Termen depasit'
        ELSE 'In curs - In termen'
    END AS StatusImprumut,
    DECODE(i.penalizare_aplicata, 1, 'S-a aplicat penalizare', 0, 'Fara penalizare', 'Necunoscut') AS DescrierePenalizare,
    SUBSTR(u.email, 1, INSTR(u.email, '@') - 1) AS UtilizatorEmailLocalPart ---Extrage partea din email până la @
FROM
    IMPRUMUT i
JOIN
    UTILIZATOR u ON i.id_utilizator = u.id_utilizator
JOIN
    EXEMPLAR ex ON i.id_exemplar = ex.id_exemplar
JOIN
    CARTE c ON ex.id_carte = c.id_carte
ORDER BY
    u.nume_complet, i.data_imprumut;

