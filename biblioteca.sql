CREATE SEQUENCE seq_id_utilizator_unic
    START WITH 1
    INCREMENT BY 1;

CREATE TABLE EDITURA (
    id_editura NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nume VARCHAR2(100) NOT NULL,
    oras VARCHAR2(100),
    an_infiintare NUMBER(4),
    CONSTRAINT uq_editura_nume UNIQUE (nume)
);

CREATE TABLE AUTOR (
    id_autor NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nume VARCHAR2(100) NOT NULL,
    prenume VARCHAR2(100),
    nationalitate VARCHAR2(50),
    data_nasterii DATE
);

-- UTILIZATOR va folosi secvența dedicată 'seq_id_utilizator_unic'
CREATE TABLE UTILIZATOR (
    id_utilizator INT DEFAULT seq_id_utilizator_unic.NEXTVAL PRIMARY KEY,
    nume_complet VARCHAR2(100) NOT NULL,
    email VARCHAR2(100) NOT NULL,
    categorie VARCHAR2(20) NOT NULL,
    data_inregistrare DATE DEFAULT SYSDATE,
    are_intarzieri NUMBER(1) DEFAULT 0,
    CONSTRAINT uq_utilizator_email UNIQUE (email),
    CONSTRAINT chk_utilizator_categorie CHECK (categorie IN ('student', 'cadru_didactic', 'extern', 'pensionar')),
    CONSTRAINT chk_utilizator_intarzieri CHECK (are_intarzieri IN (0, 1))
);

CREATE TABLE CARTE (
    id_carte NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_editura INT NOT NULL,
    titlu VARCHAR2(255) NOT NULL,
    an_aparitie NUMBER(4),
    domeniu VARCHAR2(100),
    limba VARCHAR2(50) DEFAULT 'Romana',
    CONSTRAINT fk_carte_editura FOREIGN KEY (id_editura) REFERENCES EDITURA(id_editura)
);

CREATE TABLE CARTE_AUTOR (
    id_carte INT NOT NULL,
    id_autor INT NOT NULL,
    rol_autor VARCHAR2(50) DEFAULT 'Autor principal',
    CONSTRAINT pk_carte_autor PRIMARY KEY (id_carte, id_autor),
    CONSTRAINT fk_ca_carte FOREIGN KEY (id_carte) REFERENCES CARTE(id_carte) ON DELETE CASCADE,
    CONSTRAINT fk_ca_autor FOREIGN KEY (id_autor) REFERENCES AUTOR(id_autor) ON DELETE CASCADE
);

CREATE TABLE EXEMPLAR (
    id_exemplar NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Folosim IDENTITY aici
    id_carte INT NOT NULL,
    stare VARCHAR2(20) DEFAULT 'buna',
    este_disponibil NUMBER(1) DEFAULT 1,
    locatie VARCHAR2(50),
    CONSTRAINT fk_exemplar_carte FOREIGN KEY (id_carte) REFERENCES CARTE(id_carte),
    CONSTRAINT chk_exemplar_stare CHECK (stare IN ('noua', 'buna', 'uzata', 'deteriorata', 'pierduta')),
    CONSTRAINT chk_exemplar_disponibil CHECK (este_disponibil IN (0, 1))
);

CREATE TABLE IMPRUMUT (
    id_imprumut NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_utilizator INT NOT NULL,
    id_exemplar INT NOT NULL,
    data_imprumut DATE DEFAULT SYSDATE NOT NULL,
    data_scadenta DATE NOT NULL,
    data_returnare DATE,
    penalizare_aplicata NUMBER(1) DEFAULT 0,
    CONSTRAINT uq_imprumut_exemplar_activ UNIQUE (id_exemplar),
    CONSTRAINT fk_imprumut_utilizator FOREIGN KEY (id_utilizator) REFERENCES UTILIZATOR(id_utilizator),
    CONSTRAINT fk_imprumut_exemplar FOREIGN KEY (id_exemplar) REFERENCES EXEMPLAR(id_exemplar),
    CONSTRAINT chk_imprumut_date CHECK (data_returnare IS NULL OR data_returnare >= data_imprumut),
    CONSTRAINT chk_imprumut_scadenta CHECK (data_scadenta > data_imprumut),
    CONSTRAINT chk_imprumut_penalizare CHECK (penalizare_aplicata IN (0, 1))
);

CREATE TABLE REZERVARE (
    id_rezervare NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Folosim IDENTITY aici
    id_utilizator INT NOT NULL,
    id_exemplar INT NOT NULL,
    data_rezervare DATE DEFAULT SYSDATE NOT NULL,
    status VARCHAR2(20) DEFAULT 'activa',
     CONSTRAINT fk_rezervare_utilizator FOREIGN KEY (id_utilizator) REFERENCES UTILIZATOR(id_utilizator),
    CONSTRAINT fk_rezervare_exemplar FOREIGN KEY (id_exemplar) REFERENCES EXEMPLAR(id_exemplar),
    CONSTRAINT chk_rezervare_status CHECK (status IN ('activa', 'anulata_utilizator', 'anulata_sistem', 'onorata', 'expirata')),
    CONSTRAINT uq_rezervare_activa_exemplar UNIQUE (id_exemplar, status)
);


