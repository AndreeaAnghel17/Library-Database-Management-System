  i.id_exemplar,
  i.data_imprumut,
  i.data_scadenta,
  i.data_returnare,
  i.penalizare_aplicata,
  u.nume_complet,
  u.email,
  -- Câmp calculat: numărul de zile cu care s-a depășit data_scadenta
  CASE
    WHEN i.data_returnare IS NULL
         AND SYSDATE > i.data_scadenta
      THEN TRUNC(SYSDATE - i.data_scadenta)
    WHEN i.data_returnare IS NOT NULL
         AND i.data_returnare > i.data_scadenta
      THEN TRUNC(i.data_returnare - i.data_scadenta)
    ELSE 0
  END AS zile_intarziere
FROM IMPRUMUT i
JOIN UTILIZATOR u
  ON i.id_utilizator = u.id_utilizator;


