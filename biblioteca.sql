-- publicate de edituri înființate după anul 1990. Se vor considera doar autorii care au cel puțin o astfel de carte.
-- Rezultatele vor fi obținute dintr-o subcerere în clauza FROM care combină autorii cu cărțile lor și editurile relevante.


WITH AutoriRomaniCarti AS (
    SELECT
        a.id_autor,
        a.nume || ' ' || a.prenume AS NumeCompletAutor,
        ca.id_carte
    FROM
        AUTOR a
    JOIN
        CARTE_AUTOR ca ON a.id_autor = ca.id_autor
    WHERE
        LOWER(a.nationalitate) = 'romana'
)
SELECT
    StatisticiAutori.NumeAutor,
    StatisticiAutori.NumarCartiPublicatePost1990
FROM (
    SELECT
        arc.NumeCompletAutor AS NumeAutor,
        COUNT(DISTINCT c.id_carte) AS NumarCartiPublicatePost1990
    FROM
        AutoriRomaniCarti arc
    JOIN
        CARTE c ON arc.id_carte = c.id_carte
    JOIN
        EDITURA e ON c.id_editura = e.id_editura
    WHERE
        e.an_infiintare > 1990
    GROUP BY
        arc.NumeCompletAutor
    HAVING
        COUNT(DISTINCT c.id_carte) > 0
) StatisticiAutori
ORDER BY
    StatisticiAutori.NumarCartiPublicatePost1990 DESC;

--Afișați pentru fiecare utilizator: numele complet, categoria (cu o descriere extinsă folosind DECODE), numărul
-- total de rezervări active și vechimea contului în luni. Includeți și prima literă a prenumelui și numele de familie.
-- Se vor afișa toți utilizatorii, chiar dacă nu au rezervări active (numărul de rezervări va fi 0).
-- Ordonare după vechimea contului descrescător, apoi după nume.

SELECT
    SUBSTR(u.nume_complet, INSTR(u.nume_complet, ' ') + 1) || ', ' || SUBSTR(u.nume_complet, 1, 1) || '.' AS NumeFormatat,
    DECODE(u.categorie,
           'student', 'Student Universitar',
           'cadru_didactic', 'Cadru Didactic Universitar',
           'extern', 'Utilizator Extern Bibliotecii',
           'pensionar', 'Utilizator Pensionar',
           'Categorie Necunoscuta') AS CategorieExtinsa,
    NVL(RezervariUtilizator.NumarRezervariActive, 0) AS TotalRezervariActive,
    FLOOR(MONTHS_BETWEEN(SYSDATE, u.data_inregistrare)) AS VechimeContLuni,
    TO_CHAR(u.data_inregistrare, 'Month YYYY', 'NLS_DATE_LANGUAGE=Romanian') AS LunaAnInregistrare
FROM
    UTILIZATOR u
LEFT JOIN (
    SELECT
        id_utilizator,
        COUNT(id_rezervare) AS NumarRezervariActive
    FROM
        REZERVARE
    WHERE
        status = 'activa'
    GROUP BY
        id_utilizator
) RezervariUtilizator ON u.id_utilizator = RezervariUtilizator.id_utilizator
ORDER BY
    VechimeContLuni DESC, u.nume_complet;

