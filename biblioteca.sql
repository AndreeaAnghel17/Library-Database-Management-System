-----Afișați numele complet, emailul și categoria utilizatorilor care au cel puțin un
-- împrumut activ (nereturnat) pentru o carte publicată de o editură din 'Bucuresti'.
-- Se vor afișa doar utilizatorii care au exact un astfel de împrumut activ.
-- Se va ordona după numele complet al utilizatorului.
SELECT
    u.nume_complet,
    u.email,
    u.categorie
FROM
    UTILIZATOR u
WHERE
    EXISTS (
        SELECT 1
        FROM IMPRUMUT i
        JOIN EXEMPLAR ex ON i.id_exemplar = ex.id_exemplar
        JOIN CARTE c ON ex.id_carte = c.id_carte
        JOIN EDITURA e ON c.id_editura = e.id_editura
        WHERE i.id_utilizator = u.id_utilizator
          AND e.oras = 'Bucuresti'
          AND i.data_returnare IS NULL
    )
    AND 1 = ( -- Subcerere sincronizata pentru a numara imprumuturile active de la edituri din Bucuresti
        SELECT COUNT(*)
        FROM IMPRUMUT i_count
        JOIN EXEMPLAR ex_count ON i_count.id_exemplar = ex_count.id_exemplar
        JOIN CARTE c_count ON ex_count.id_carte = c_count.id_carte
        JOIN EDITURA e_count ON c_count.id_editura = e_count.id_editura
        WHERE i_count.id_utilizator = u.id_utilizator
          AND e_count.oras = 'Bucuresti'
          AND i_count.data_returnare IS NULL
    )
ORDER BY
    u.nume_complet;


