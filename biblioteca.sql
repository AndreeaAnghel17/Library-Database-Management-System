--Cerere cu outer join pe cel puțin 4 tabele
--Afișează toţi utilizatorii din bibliotecă împreună cu
-- informațiile despre cartea pe care au împrumutat-o (titlu), cu
-- locația exemplarului și cu numele editurii. Dacă un utilizator nu a făcut niciun împrumut, rândul
-- va apărea oricum, dar coloanele legate de împrumut, exemplare, carte şi editură vor fi NULL.

SELECT
    u.id_utilizator,
    u.nume_complet,
    i.id_imprumut,
    ex.id_exemplar,
    c.titlu AS titlu_carte,
    ex.locatie       AS locatie_exemplar,
    e.nume           AS nume_editura
FROM UTILIZATOR u
LEFT OUTER JOIN IMPRUMUT     i  ON u.id_utilizator = i.id_utilizator
LEFT OUTER JOIN EXEMPLAR     ex ON i.id_exemplar   = ex.id_exemplar
LEFT OUTER JOIN CARTE        c  ON ex.id_carte      = c.id_carte
LEFT OUTER JOIN EDITURA      e  ON c.id_editura     = e.id_editura
ORDER BY u.nume_complet;


--Cerere care foloseşte operația “division” (÷):
--Găsește utilizatorii (id și nume) care au împrumutat fiecare carte publicată de editura Humanitas.

SELECT
    u.id_utilizator,
    u.nume_complet
FROM UTILIZATOR u
WHERE NOT EXISTS (
    -- Subinterogarea 1: ia toate cărțile publicate de "Humanitas"
    SELECT c.id_carte
    FROM CARTE c
    JOIN EDITURA e
      ON c.id_editura = e.id_editura
    WHERE e.nume = 'Humanitas'
      AND NOT EXISTS (
        -- Subinterogarea 2: să existe un împrumut făcut de u pentru fiecare id_carte
        SELECT 1
        FROM IMPRUMUT i
        JOIN EXEMPLAR ex
          ON i.id_exemplar = ex.id_exemplar
        WHERE i.id_utilizator = u.id_utilizator
          AND ex.id_carte      = c.id_carte
      )
)
ORDER BY u.nume_complet;


--Afișează primele 3 titluri de carte care au fost împrumutate de cele mai multe ori (adică titlurile pentru care numărul
-- total de împrumuturi este în top 3). Pentru fiecare titlu, precizează și numărul total de împrumuturi.
SELECT *
FROM (
    SELECT
        c.titlu,
        COUNT(*) AS numar_imprumuturi
    FROM IMPRUMUT i
    JOIN EXEMPLAR ex
      ON i.id_exemplar = ex.id_exemplar
    JOIN CARTE c
      ON ex.id_carte    = c.id_carte
    GROUP BY c.titlu
    ORDER BY COUNT(*) DESC
)
WHERE ROWNUM <= 3;













